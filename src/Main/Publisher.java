    private void btAddNXBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddNXBActionPerformed
        // TODO add your handling code here:
         if(this.txtmanxb.getText().length()==0)
            JOptionPane.showMessageDialog(null,"ma nha xuat khong duoc bo trong","thong bao",1);
        else 
            if(this.txtmanxb.getText().length()>10)
                JOptionPane.showMessageDialog(null,"ma tac gia khong duoc vuot qua 10 ki tu","thong bao",1);
        else
                if(this.txtTenNXB.getText().length() ==0)
                    JOptionPane.showMessageDialog(null,"tên tác giả không được bỏ trống","thông báo",1);
                else if(this.txtdiachi.getText().length()== 0)
                    JOptionPane.showMessageDialog(null,"tên địa chỉ không được bỏ trống");
                else if(this.txtphone.getText().length() ==0)
                    JOptionPane.showMessageDialog(null,"tên phone không được bỏ trống","thông báo",1);
                else {
                    NXB.InsertNXB(this.txtmanxb.getText().trim(),this.txtTenNXB.getText(),this.txtdiachi.getText(),this.txtphone.getText());
                    ShowNXB();
                    ProcessNXB(false);
                }
    }//GEN-LAST:event_btAddNXBActionPerformed

