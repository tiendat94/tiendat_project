    private void tableNXBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNXBMouseClicked
        // TODO add your handling code here:
         ProcessNXB(true);
        this.btAddNXB.setEnabled(false);
        try{
        int row = this.tableNXB.getSelectedRow();
        String IDrow = (this.tableNXB.getModel().getValueAt(row, 0).toString());
        String sql2=" SELECT * FROM quanlythuvien.nhaxuatban where MaNXB = '"+IDrow+"'";
        ResultSet rs = UpdateTable.ShowTextField(sql2);
        if(rs.next()){
            malop = rs.getString("MaNXB");
            this.txtmanxb.setText(rs.getString("MaNXB"));
            this.txtTenNXB.setText((rs.getString("TenNXB")));
            this.txtdiachi.setText(rs.getString("DiaChi"));
            this.txtphone.setText(rs.getString("DienThoai"));
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }
    }//GEN-LAST:event_tableNXBMouseClicked

