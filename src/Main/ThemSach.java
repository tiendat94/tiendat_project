    public boolean check(String str, Vector<String> vt){
        int size = vt.size();
        for (int i = 0; i < size; i++){
            if (vt.get(i).equals(str) == true){
                return true;
            }
        }
        return false;
    }
    
    public int number(String str){
        if (str.equals("")){
            return 0;
        }
        return Integer.parseInt(str);
    }
    
    public String muon(){
        if (radio.isSelected() == true){
            return "1";
        }
        return "0";
    }
    
    public int laymasach(Vector<String> vt){
        int size = vt.size();
        int tg = 1;
        for (int i = 0; i < size; i++){
            
            if (tg == Integer.parseInt(vt.get(i))){
                tg ++;
            }else {
                break;
            }
            
        }
        return tg;
    }

