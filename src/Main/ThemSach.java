    private void bon_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bon_ThemActionPerformed
        // TODO add your handling code here:
        if (check == true){
            return;
        }
        check = true;
        int a = 0;
        String matuasach = txt_MaTuaSach.getText();
        Vector<String> vt; 
        String tensach = txt_TenSach.getText();
        String muon = muon();
        String giasach = txt_GiaSach.getText();
        String sotrang = txt_SoTrang.getText();
        String mangonngu = txt_MaNgonNgu.getText();
        String matacgia = txt_MaTacGia.getText();
        String maNXB = txt_MaNXB.getText();
        
        String malinhvuc = txt_MaLinhVuc.getText();
        
        if (tensach.equals("")==true){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên sách");
            check = false;
            return;
        }
        if (giasach.equals(null)){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập giá sách");
            check = false;
            return;
        }else {
            try{
                a = Integer.parseInt(giasach);
            }catch(NumberFormatException e1){
                JOptionPane.showMessageDialog(null, "Giá sách bị sai. Xin vui lòng nhập lại");
                check = false;
                return;
            }
            
        }
        
        if(sotrang.equals("")){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập số trang của sách");
            check = false;
            return;
        }else{
            try{
                a = Integer.parseInt(sotrang);
            }catch(NumberFormatException e2){
                JOptionPane.showMessageDialog(null, "Số trang bị sai. Xin vui lòng nhập lại");
                check = false;
                return;
            }
        }
        vt = Them.truyvan("select distinct MaNgonNgu from tuasach where MaNgonNgu is not null");
        if (mangonngu.length() != 5){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã ngôn ngữ hoặc nhập sai mã ngôn ngữ. Xin vui lòng kiểm tra lại");
            check = false;
            return;
        }else if (check(mangonngu,vt) == false){
            JOptionPane.showMessageDialog(null, "Mã ngôn ngữ hiện không có trong CSDL. XIn vui lòng kiểm tra lại hoặc thêm mã ngôn ngữ vào dữ liệu");
            check = false;
            return;
        }
        
        vt = Them.truyvan("select distinct MaTG from tuasach where MaTG is not null");
        if (matacgia.length() != 5){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã tác giả hoặc nhập sai mã tác giả. Xin vui lòng kiểm tra lại");
            check = false;
            return;
        }else if (check(matacgia,vt) == false){
            JOptionPane.showMessageDialog(null, "Mã tacgia hiện không có trong CSDL. XIn vui lòng kiểm tra lại hoặc thêm mã tác giả vào dữ liệu");
            check = false;
            return;
        }
        
        vt = Them.truyvan("select distinct MaNXB from tuasach where MaNXB is not null");
        if (maNXB.length() != 6){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã nhà xuất bản hoặc nhập sai mã nhaf xuất bản. Xin vui lòng kiểm tra lại");
            check = false;
            return;
        }else if (check(maNXB,vt) == false){
            JOptionPane.showMessageDialog(null, "Mã nhà xuất bản hiện không có trong CSDL. Xin vui lòng kiểm tra lại hoặc thêm mã nha xuất bản vào dữ liệu");
            check = false;
            return;
        }
        
        vt = Them.truyvan("select distinct maLV from tuasach where maLV is not null");
        if (malinhvuc.length() != 5){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã lĩnh vực hoặc nhập sai mã linhx vực. Xin vui lòng kiểm tra lại");
            check = false;
            return;
        }else if (check(malinhvuc,vt) == false){
            JOptionPane.showMessageDialog(null, "Mã linh vực hiện không có trong CSDL. XIn vui lòng kiểm tra lại hoặc thêm mã lĩnh vực vào dữ liệu");
            check = false;
            return;
        }
        
        
        vt = Them.truyvan("select MaTuaSach from tuasach where tuasach.matuasach is not null");
        if (matuasach.length() != 2){
            JOptionPane.showMessageDialog(null, "Mã tựa sách đã sai");
            check = false;
            return;
        }else if (check(matuasach,vt) == false){
            Them.themSach("INSERT INTO tuasach (`MaTuaSach`, `TenSach`, `ChoMuon`, `GiaSach`, `SoTrang`, "
                    + "`MaNgonNgu`, `MaTG`, `MaNXB`, `MaLV`) VALUES ('"+matuasach+"', '"+tensach+"', "+muon()+", '"+giasach+"', '"+sotrang+"', '"+mangonngu+"', '"+matacgia+"', '"+maNXB+"', '"+malinhvuc+"')");
            
        }else {
            String str = truyvanxau("select tensach from tuasach where matuasach = '"+txt_MaTuaSach.getText()+"'");
            if (txt_TenSach.getText().equals(str)){
                JOptionPane.showMessageDialog(null, "Tên sách bị sai");
                return;
            }
        }
        
        int number = Integer.parseInt(txt_SoLuong.getText());
        int value = 0;
        for (int i = 0 ; i < number ; i++){
            vt = Them.truyvan("select masach from sach order by masach asc");
            value = laymasach(vt);
            Them.themSach("insert into sach values ('"+value+"','"+matuasach+"')");
        }
        JOptionPane.showMessageDialog(null, "Bạn đã thêm sách thành công");
        txt_TenSach.setText("");
        txt_MaTuaSach.setText("");
        txt_GiaSach.setText("");
        txt_SoTrang.setText("");
        txt_SoLuong.setText("");
        txt_MaNgonNgu.setText("");
        txt_MaTacGia.setText("");
        txt_MaNXB.setText("");
        txt_MaLinhVuc.setText("");
        
    }//GEN-LAST:event_bon_ThemActionPerformed

